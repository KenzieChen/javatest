public class LastLaugh{
	public static void main(String[] args){
		//字符串拼接
		System.out.println("H"+"a");
		//先转换为整数，相加在打印
		System.out.println('H'+'a');
		//将char转换为String，再拼接
		System.out.println(""+'H'+'a');
		//先拼接
		System.out.println("1+1="+1+1);
		//先计算
		System.out.println(1+1+"=1+1");
		//与C语言有点像
		System.out.printf("%c%c",'H','a');
	}
}
