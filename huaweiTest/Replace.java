public class Replace{
	public static void main(String[] args){
		String input = "yiersansiwuliuqibajiu";
		String output =input.replace("yi","1").replace("er","2").replace("san","3").replace("si","4").replace("wu","5").replace("liu","6").replace("qi","7").replace("ba","8").replace("jiu","9");
		System.out.println(output);
	}
}
